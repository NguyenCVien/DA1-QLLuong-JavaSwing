    private void tblTaiKhoanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTaiKhoanMouseClicked
        try {
            int id = tblTaiKhoan.rowAtPoint(evt.getPoint());
            String manv = tblTaiKhoan.getValueAt(id, 0).toString();
            TaiKhoan tk = dao.getOneNhanVienByMaNV(manv);
            setModel(tk);
        } catch (Exception e) {
            //JOptionPane.showMessageDialog(this, "Error: MouseClick" + e.getMessage());
            //e.printStackTrace();
        }
    }//GEN-LAST:event_tblTaiKhoanMouseClicked

    private void btnSeachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeachActionPerformed
        if (txtMaNhanVien.getText().isEmpty()) {
            MsgBox.alert(this, "Bạn chưa nhập mã nhân viên");
        } else {
            TaiKhoan tk = dao.getOneNhanVienByMaNV(txtMaNhanVien.getText());
            if (tk != null) {
                TaiKhoan d = dao.getOneNhanVienByMaNV(tk.getMaNV());
                if (d != null) {
                    setModel(d);
                } else {
                    txtMaNhanVien.setText(tk.getMaNV());
                    txtTenTK.setText(tk.getTenTaiKhoan());
                    txtMatKhau.setText(tk.getMatKhau());
                    rdoTruongPhong.setSelected(tk.getVaiTro());
//                    rdoTruongPhong.setSelected(tk.getVaiTro() == 1);
                    rdoNhanVien.setSelected(tk.getVaiTro());
//                    rdoNhanVien.setSelected(tk.getVaiTro() == 0);
                }
            } else {
                MsgBox.alert(this, "Mã nhân viên không đúng");
            }
        }
    }//GEN-LAST:event_btnSeachActionPerformed


