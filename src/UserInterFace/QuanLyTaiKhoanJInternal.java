    public TaiKhoan getModel() throws ParseException {
        TaiKhoan tk = new TaiKhoan();
        tk.setMaNV(txtMaNhanVien.getText());
        tk.setTenTaiKhoan(txtTenTK.getText());
        tk.setMatKhau(txtMatKhau.getText());
        tk.setVaiTro(rdoTruongPhong.isSelected());
        //tk.setVaiTro(rdoTruongPhong.isSelected() ? 1 : 0);
        //tk.setVaiTro(Integer.parseInt(txtVaiTro.getText()));

        return tk;
    }

    public void setModel(TaiKhoan tk) {
        txtMaNhanVien.setText(tk.getMaNV());
        txtTenTK.setText(tk.getTenTaiKhoan());
        txtMatKhau.setText(tk.getMatKhau());
        rdoTruongPhong.setSelected(tk.getVaiTro()==true);
        rdoNhanVien.setSelected(tk.getVaiTro()==false);
    }

