    public boolean checkForm() {
        if(txtMaNhanVien.getText().equals("")){
            MsgBox.alert(this, "Mã nhân viên không được để trống");
            return false;
        }
        if(txtTenTK.getText().equals("")){
            MsgBox.alert(this, "Tên tài khoản không được để trống");
            return false;
        }
        if(txtMatKhau.getPassword().equals("")){
            MsgBox.alert(this, "Mật khẩu không được để trống");
            return false;
        }
        return true;
    }
    
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (checkForm()) {
            try {
                TaiKhoan sv = getModel();
                if (dao.add(sv) > 0) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công!");
                    fillToTable();
                }
            } catch (Exception e) {
                MsgBox.alert(this, "Thêm thất bại, mã nhân viên đã tồn tại");
            }
        } else {
            MsgBox.alert(this, "Bạn chưa nhập đầy đủ thông tin");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (checkForm()) {
            try {
                TaiKhoan tk = getModel();
                if (dao.updateTaiKhoanByID(tk) > 0) {
                    MsgBox.alert(this, "Cập nhật thành công");
                    fillToTable();
                }
            } catch (Exception e) {
            }
        } else {
            MsgBox.alert(this, "Bạn chưa nhập thông tin");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (txtMaNhanVien.getText().isEmpty()) {
            MsgBox.alert(this, "Bạn chưa nhập mã nhân viên để xóa");
        } else {
            if (MsgBox.confirm(this, "Bạn chắc chắn muốn xóa?")) {
                if (dao.delTaiKhoanByID(txtMaNhanVien.getText()) > 0) {
                    MsgBox.alert(this, "Xóa thành công");
                    fillToTable();
                    reset();
                } else {
                    MsgBox.alert(this, "Sai mã nhân viên! Vui lòng nhập lại");
                }
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

